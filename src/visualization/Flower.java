package visualization;
import java.awt.Color;


public class Flower {
	public Color color;
	public int size;
	public int x;
	public int y;
	public int methods;
	
	public Flower(Color color, int size, int x, int y, int methods){
		this.color = color;
		this.size = size;
		this.x = x;
		this.y = y;
		this.methods = methods;
	}
}
